import { CogniMeshBIOS } from '../../src/bios/index.js';
import { BaseClient } from '../../src/clients/base-client.js';

// ============================================================
// Codex Client Example
// ============================================================

console.log('[CogniMesh v5.0] Codex Client Example');
console.log('======================================\n');

// Simulated Codex Client
class CodexClient extends BaseClient {
  constructor(config = {}) {
    super({
      name: config.name || 'Codex',
      provider: 'openai',
      ...config
    });
    this.copilotMode = config.copilotMode ?? false;
    this.ide = config.ide || 'vscode';
    this.fastMode = config.fastMode ?? true;
  }

  async initialize() {
    this.status = 'connected';
    this.health.connected = true;
    this.emit('connected');
    console.log('✅ Codex client connected');
  }

  getCapabilities() {
    return {
      languages: ['javascript', 'typescript', 'python', 'go', 'rust', 'java', 'cpp', 'csharp'],
      domains: ['code-generation', 'completion', 'refactoring', 'testing'],
      maxContextTokens: 8000,
      supportsStreaming: true,
      supportsVision: false,
      supportsFunctionCalling: true,
      copilotMode: this.copilotMode,
      ideIntegration: ['vscode', 'jetbrains', 'vim', 'neovim'],
      responseSpeed: this.fastMode ? 'fast' : 'balanced'
    };
  }

  async execute(task, options = {}) {
    console.log(`\n⚡ Codex executing: ${task.type}`);
    console.log(`   Description: ${task.description || 'N/A'}`);

    // Simulate fast response
    const delay = this.fastMode ? 50 : 200;
    await this._simulateDelay(delay);

    switch (task.type) {
      case 'generate-function':
        return this._generateFunction(task);
      case 'complete-code':
        return this._completeCode(task);
      case 'generate-tests':
        return this._generateTests(task);
      case 'copilot-suggest':
        return this._copilotSuggest(task);
      case 'refactor-snippet':
        return this._refactorSnippet(task);
      default:
        return { result: 'Task completed', confidence: 0.92 };
    }
  }

  async _generateFunction(task) {
    const functionName = task.functionName || 'processData';
    const language = task.language || 'javascript';

    const code = `// Generated by Codex
/**
 * ${task.description || 'Process input data'}
 * @param {Object} data - Input data
 * @returns {Object} Processed result
 */
function ${functionName}(data) {
  // Validate input
  if (!isValid(data)) {
    throw new Error('Invalid input provided');
  }
  
  // Transform data
  const normalized = normalize(data);
  const enriched = enrich(normalized);
  
  // Return processed result
  return {
    success: true,
    data: enriched,
    timestamp: Date.now()
  };
}

// Helper functions
function isValid(data) {
  return data && typeof data === 'object';
}

function normalize(data) {
  return Object.entries(data).reduce((acc, [key, value]) => {
    acc[key.toLowerCase()] = value;
    return acc;
  }, {});
}

function enrich(data) {
  return { ...data, _processed: true };
}`;

    return {
      code,
      language,
      lines: code.split('\n').length,
      complexity: { cyclomatic: 2, cognitive: 4 },
      confidence: 0.95,
      generationTime: '120ms'
    };
  }

  async _completeCode(task) {
    const prefix = task.prefix || '';
    const suffix = task.suffix || '';

    const completion = `  const results = await Promise.all(
    items.map(async (item) => {
      const processed = await processItem(item);
      return validate(processed) ? processed : null;
    })
  );
  
  return results.filter(Boolean);`;

    return {
      completion,
      prefix,
      suffix,
      confidence: 0.94,
      alternatives: [
        '// Alternative implementation\nconst results = [];\nfor (const item of items) {\n  results.push(await processItem(item));\n}'
      ]
    };
  }

  async _generateTests(task) {
    const functionName = task.functionName || 'processData';

    return {
      testFile: `${functionName}.test.js`,
      framework: 'jest',
      tests: [
        {
          name: `should process valid ${functionName} input`,
          code: `test('should process valid input', () => {\n  const input = { name: 'test', value: 42 };\n  const result = ${functionName}(input);\n  expect(result.success).toBe(true);\n  expect(result.data).toBeDefined();\n});`,
          coverage: ['happy-path']
        },
        {
          name: `should reject invalid ${functionName} input`,
          code: `test('should reject invalid input', () => {\n  expect(() => ${functionName}(null)).toThrow('Invalid input');\n  expect(() => ${functionName}('string')).toThrow('Invalid input');\n});`,
          coverage: ['error-handling']
        },
        {
          name: `should handle edge cases`,
          code: `test('should handle edge cases', () => {\n  const input = {};\n  const result = ${functionName}(input);\n  expect(result.success).toBe(true);\n});`,
          coverage: ['edge-cases']
        }
      ],
      totalTests: 3,
      estimatedCoverage: '87%'
    };
  }

  async _copilotSuggest(task) {
    if (!this.copilotMode) {
      return { message: 'Copilot mode disabled', suggestions: [] };
    }

    const context = task.context || '';
    const cursor = task.cursorPosition || { line: 1, column: 1 };

    return {
      mode: 'copilot',
      suggestions: [
        {
          type: 'completion',
          text: 'await db.connect();',
          description: 'Connect to database',
          confidence: 0.91
        },
        {
          type: 'import',
          text: "import { connection } from './db';",
          description: 'Import database connection',
          confidence: 0.89
        }
      ],
      cursorPosition: cursor,
      ide: this.ide
    };
  }

  async _refactorSnippet(task) {
    const code = task.code || '';
    
    return {
      original: code,
      refactored: code.replace(/var /g, 'const ')
                      .replace(/function\s*\(/g, '(')
                      .replace(/\)\s*\{/g, ') => {'),
      changes: [
        { type: 'modernize', from: 'var', to: 'const/let' },
        { type: 'arrow-function', from: 'function', to: '=>' },
        { type: 'formatting', from: 'inconsistent', to: 'standard' }
      ],
      improvements: {
        readability: '+25%',
        maintainability: '+15%'
      }
    };
  }

  async _simulateDelay(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
  }
}

// ============================================================
// Main execution
// ============================================================

async function main() {
  const bios = new CogniMeshBIOS();
  await bios.boot();

  try {
    // Initialize Codex client
    const codex = new CodexClient({
      name: 'Codex-latest',
      copilotMode: true,
      ide: 'vscode',
      fastMode: true
    });

    await codex.initialize();

    // Display capabilities
    console.log('\n--- Client Capabilities ---\n');
    const caps = codex.getCapabilities();
    console.log(`Languages: ${caps.languages.slice(0, 5).join(', ')}...`);
    console.log(`Copilot Mode: ${caps.copilotMode ? 'enabled' : 'disabled'}`);
    console.log(`IDE Integration: ${caps.ideIntegration.join(', ')}`);
    console.log(`Response Speed: ${caps.responseSpeed}`);

    // Task 1: Generate function
    console.log('\n--- Task 1: Generate Function ---\n');
    const genResult = await codex.execute({
      type: 'generate-function',
      description: 'Create data processing function',
      functionName: 'transformUserData',
      language: 'javascript'
    });

    console.log(`Generated ${genResult.lines} lines of ${genResult.language}`);
    console.log(`Complexity: cyclomatic=${genResult.complexity.cyclomatic}`);
    console.log(`Confidence: ${genResult.confidence}`);
    console.log(`\nCode Preview:\n${genResult.code.substring(0, 300)}...`);

    // Task 2: Complete code
    console.log('\n--- Task 2: Code Completion ---\n');
    const completeResult = await codex.execute({
      type: 'complete-code',
      prefix: 'async function processItems(items) {',
      suffix: '}'
    });

    console.log('Completion:');
    console.log(completeResult.completion);
    console.log(`\nConfidence: ${completeResult.confidence}`);

    // Task 3: Generate tests
    console.log('\n--- Task 3: Generate Tests ---\n');
    const testResult = await codex.execute({
      type: 'generate-tests',
      functionName: 'transformUserData',
      framework: 'jest'
    });

    console.log(`Test File: ${testResult.testFile}`);
    console.log(`Framework: ${testResult.framework}`);
    console.log(`Total Tests: ${testResult.totalTests}`);
    console.log(`Estimated Coverage: ${testResult.estimatedCoverage}`);
    console.log('\nTest Cases:');
    testResult.tests.forEach((test, i) => {
      console.log(`  ${i + 1}. ${test.name}`);
      console.log(`     Coverage: ${test.coverage.join(', ')}`);
    });

    // Task 4: Copilot suggestions
    console.log('\n--- Task 4: Copilot Suggestions ---\n');
    const copilotResult = await codex.execute({
      type: 'copilot-suggest',
      context: 'function init() {',
      cursorPosition: { line: 1, column: 20 }
    });

    if (copilotResult.suggestions.length > 0) {
      console.log('Suggestions:');
      copilotResult.suggestions.forEach((sugg, i) => {
        console.log(`  ${i + 1}. [${sugg.type}] ${sugg.text}`);
        console.log(`     ${sugg.description} (${sugg.confidence})`);
      });
    }

    // Task 5: Refactor snippet
    console.log('\n--- Task 5: Refactor Snippet ---\n');
    const refactorResult = await codex.execute({
      type: 'refactor-snippet',
      code: `var data = getData();
function process(x) {
  return x * 2;
}`
    });

    console.log('Refactoring Changes:');
    refactorResult.changes.forEach(change => {
      console.log(`  • ${change.type}: ${change.from} → ${change.to}`);
    });
    console.log(`\nReadability: ${refactorResult.improvements.readability}`);
    console.log(`Maintainability: ${refactorResult.improvements.maintainability}`);

    console.log('\n✅ Codex example complete!');

  } catch (error) {
    console.error('\n❌ Error:', error.message);
  } finally {
    await bios.shutdown();
  }
}

main();

